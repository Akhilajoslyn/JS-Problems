function getDataType(input) {
    return typeof input;
}

// Example 
let example1 = 42;
let example2 = "hello";
let example3 = true;
let example4 = {name: "Akhila", age: 22};
let example5 = [1, 2, 3, 4, 5];

console.log(getDataType(example1)); 
console.log(getDataType(example2)); 
console.log(getDataType(example3)); 
console.log(getDataType(example4)); 
console.log(getDataType(example5)); 
