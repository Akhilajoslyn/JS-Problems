function oppositeSign(num) {
    return -num;
}

// Example usage:
let positiveNum = 42;
let negativeNum = -15;

console.log(`Opposite of ${positiveNum} is:`, oppositeSign(positiveNum)); // Output: Opposite of 42 is: -42
console.log(`Opposite of ${negativeNum} is:`, oppositeSign(negativeNum)); // Output: Opposite of -15 is: 15
